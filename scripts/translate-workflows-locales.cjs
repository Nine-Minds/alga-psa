// Recursively walk the English JSON and apply the translation for each leaf string.
// If no translation is available, the English value is kept (valid fallback via defaultValue).
function translateTree(node, dict) {
  if (typeof node === 'string') {
    return dict[node] ?? node;
  }
  if (Array.isArray(node)) {
    return node.map((item) => translateTree(item, dict));
  }
  if (node && typeof node === 'object') {
    const out = {};
    for (const [k, v] of Object.entries(node)) {
      out[k] = translateTree(v, dict);
    }
    return out;
  }
  return node;
}

function main() {
  const enPath = path.join(REPO_ROOT, 'server/public/locales/en/msp/workflows.json');
  const en = JSON.parse(fs.readFileSync(enPath, 'utf8'));

  const locales = ['fr', 'es', 'de', 'nl', 'it', 'pl'];
  let totalTranslatedKeys = 0;
  for (const loc of locales) {
    const dict = translations[loc] || {};
    const translated = translateTree(en, dict);
    const outPath = path.join(REPO_ROOT, `server/public/locales/${loc}/msp/workflows.json`);
    fs.writeFileSync(outPath, JSON.stringify(translated, null, 2) + '\n');
    // Count how many unique English values have an override
    const keys = Object.keys(dict);
    console.log(`${loc}: ${keys.length} unique overrides applied → ${outPath}`);
    totalTranslatedKeys += keys.length;
  }
  console.log(`\nTotal unique translation overrides applied across 6 locales: ${totalTranslatedKeys}`);
}

main();
