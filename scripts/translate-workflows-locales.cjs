// Recursively walk the English JSON and apply the translation for each leaf string.
// Resolution order: 1) per-language keyOverrides by dotted path, 2) per-language value-map,
// 3) English value as-is (valid fallback via i18next defaultValue).
function translateTree(node, dict, keyMap, prefix = '') {
  if (typeof node === 'string') {
    if (keyMap && Object.prototype.hasOwnProperty.call(keyMap, prefix)) return keyMap[prefix];
    return dict[node] ?? node;
  }
  if (Array.isArray(node)) {
    return node.map((item, i) => translateTree(item, dict, keyMap, prefix ? prefix + '[' + i + ']' : String(i)));
  }
  if (node && typeof node === 'object') {
    const out = {};
    for (const [k, v] of Object.entries(node)) {
      out[k] = translateTree(v, dict, keyMap, prefix ? prefix + '.' + k : k);
    }
    return out;
  }
  return node;
}

function main() {
  const enPath = path.join(REPO_ROOT, 'server/public/locales/en/msp/workflows.json');
  const en = JSON.parse(fs.readFileSync(enPath, 'utf8'));

  const locales = ['fr', 'es', 'de', 'nl', 'it', 'pl'];
  let totalTranslatedKeys = 0;
  for (const loc of locales) {
    const dict = translations[loc] || {};
    const keyMap = keyOverrides[loc] || {};
    const translated = translateTree(en, dict, keyMap);
    const outPath = path.join(REPO_ROOT, `server/public/locales/${loc}/msp/workflows.json`);
    fs.writeFileSync(outPath, JSON.stringify(translated, null, 2) + '\n');
    const valueKeys = Object.keys(dict).length;
    const keyKeys = Object.keys(keyMap).length;
    console.log(`${loc}: ${valueKeys} value overrides + ${keyKeys} key overrides → ${outPath}`);
    totalTranslatedKeys += valueKeys + keyKeys;
  }
  console.log(`\nTotal translation overrides applied across 6 locales: ${totalTranslatedKeys}`);
}

main();
